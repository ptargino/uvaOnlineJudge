package p10633;

import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		StringBuilder out = new StringBuilder();
		long n;
		while ((n = in.nextLong()) != 0) {
			long b = n + n / 9;
			if (n % 9 == 0)
				out.append((b - 1) + " " + b + "\n");
			else
				out.append(b + "\n");
		}
		System.out.print(out);

	}
}
