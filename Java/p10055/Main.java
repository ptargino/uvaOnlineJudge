package p10055;
import java.util.Scanner;

class Main {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		StringBuffer out = new StringBuffer();
		while (in.hasNextInt()) {
			int a = in.nextInt();
			int b = in.nextInt();
			out.append(Math.abs(b - a) + "\n");
		}
		System.out.print(out);
	}
}
